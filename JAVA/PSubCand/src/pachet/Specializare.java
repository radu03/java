package pachet;

public class Specializare {
	
	private int codSpecializare;
	private int numarLocuriSpecializare;
	
	public Specializare() {
		
	}

	public int getCodSpecializare() {
		return codSpecializare;
	}

	public void setCodSpecializare(int codSpecializare) {
		this.codSpecializare = codSpecializare;
	}

	public int getNumarLocuriSpecializare() {
		return numarLocuriSpecializare;
	}

	public void setNumarLocuriSpecializare(int numarLocuriSpecializare) {
		this.numarLocuriSpecializare = numarLocuriSpecializare;
	}
	
	

}
